const numero1 = 20;
const numero2 = 40;

//Suma
suma =  numero1 + numero2;
console.log("Suma = ",suma);
//Resta
resta = numero1 - numero2;
console.log("Resta = ",resta);
//Mutiplicación
mult = numero1 * numero2;
console.log("Multiplicación = ",mult);
//División
div = numero1 / numero2;
console.log("División = ",div);

//Módulo
mod = numero2 % numero1;
console.log("Módulo = ",mod);
